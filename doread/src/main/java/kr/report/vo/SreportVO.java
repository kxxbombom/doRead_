package kr.report.vo;

public class SreportVO {
	private int sr_num;
	private String sr_content;
	private int sr_category;
	private int mem_num;
	private int s_num;
	public int getSr_num() {
		return sr_num;
	}
	public void setSr_num(int sr_num) {
		this.sr_num = sr_num;
	}
	public String getSr_content() {
		return sr_content;
	}
	public void setSr_content(String sr_content) {
		this.sr_content = sr_content;
	}
	public int getSr_category() {
		return sr_category;
	}
	public void setSr_category(int sr_category) {
		this.sr_category = sr_category;
	}
	public int getMem_num() {
		return mem_num;
	}
	public void setMem_num(int mem_num) {
		this.mem_num = mem_num;
	}
	public int getS_num() {
		return s_num;
	}
	public void setS_num(int s_num) {
		this.s_num = s_num;
	}
	

}
