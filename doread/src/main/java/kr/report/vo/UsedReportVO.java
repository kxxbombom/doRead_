package kr.report.vo;

public class UsedReportVO {
	private int ur_num;
	private String ur_content;
	private int ur_category;
	private int mem_num;
	private int u_num;
	private int auth;
	public int getUr_num() {
		return ur_num;
	}
	public void setUr_num(int ur_num) {
		this.ur_num = ur_num;
	}
	public String getUr_content() {
		return ur_content;
	}
	public void setUr_content(String ur_content) {
		this.ur_content = ur_content;
	}
	public int getUr_category() {
		return ur_category;
	}
	public void setUr_category(int ur_category) {
		this.ur_category = ur_category;
	}
	public int getMem_num() {
		return mem_num;
	}
	public void setMem_num(int mem_num) {
		this.mem_num = mem_num;
	}
	public int getU_num() {
		return u_num;
	}
	public void setU_num(int u_num) {
		this.u_num = u_num;
	}
	public int getAuth() {
		return auth;
	}
	public void setAuth(int auth) {
		this.auth = auth;
	}
}
