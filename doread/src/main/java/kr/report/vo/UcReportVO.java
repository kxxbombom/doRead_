package kr.report.vo;

public class UcReportVO {
	private int urc_num;
	private String urc_content;
	private int urc_category;
	private int mem_num;
	private int uc_num;
	private int auth;
	private String content;
	private int u_num;
	public int getUrc_num() {
		return urc_num;
	}
	public void setUrc_num(int urc_num) {
		this.urc_num = urc_num;
	}
	public String getUrc_content() {
		return urc_content;
	}
	public void setUrc_content(String urc_content) {
		this.urc_content = urc_content;
	}
	public int getUrc_category() {
		return urc_category;
	}
	public void setUrc_category(int urc_category) {
		this.urc_category = urc_category;
	}
	public int getMem_num() {
		return mem_num;
	}
	public void setMem_num(int mem_num) {
		this.mem_num = mem_num;
	}
	public int getUc_num() {
		return uc_num;
	}
	public void setUc_num(int uc_num) {
		this.uc_num = uc_num;
	}
	public int getAuth() {
		return auth;
	}
	public void setAuth(int auth) {
		this.auth = auth;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public int getU_num() {
		return u_num;
	}
	public void setU_num(int u_num) {
		this.u_num = u_num;
	}
}
