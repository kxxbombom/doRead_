package kr.report.vo;

public class ScreportVO {
	private int src_num;
	private String src_content;
	private int src_category;
	private int mem_num;
	private int sc_num;
	
	public int getSrc_num() {
		return src_num;
	}
	public void setSrc_num(int src_num) {
		this.src_num = src_num;
	}
	public String getSrc_content() {
		return src_content;
	}
	public void setSrc_content(String src_content) {
		this.src_content = src_content;
	}
	public int getSrc_category() {
		return src_category;
	}
	public void setSrc_category(int src_category) {
		this.src_category = src_category;
	}
	public int getMem_num() {
		return mem_num;
	}
	public void setMem_num(int mem_num) {
		this.mem_num = mem_num;
	}
	public int getSc_num() {
		return sc_num;
	}
	public void setSc_num(int sc_num) {
		this.sc_num = sc_num;
	}

}
